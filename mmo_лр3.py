# 2. Одномерный поиск
# ============================================================
def golden_section_search(f, a_bound, b_bound, tol=1e-5):
    gr = (np.sqrt(5) - 1) / 2
    a_val, b_val = a_bound, b_bound
    x1 = b_val - gr * (b_val - a_val)
    x2 = a_val + gr * (b_val - a_val)
    f1, f2 = f(x1), f(x2)

    for _ in range(100):
        if abs(b_val - a_val) < tol:
            break
        if f1 < f2:
            b_val, x2, f2 = x2, x1, f1
            x1 = b_val - gr * (b_val - a_val)
            f1 = f(x1)
        else:
            a_val, x1, f1 = x1, x2, f2
            x2 = a_val + gr * (b_val - a_val)
            f2 = f(x2)
    return (a_val + b_val) / 2

def line_search(f, x, d, alpha0=1.0, tol=1e-5):
    def phi(alpha):
        return f(x + alpha * d)

    alpha = alpha0
    while phi(alpha) < phi(alpha/2) and abs(alpha) < 10:
        alpha *= 2
    if alpha > 0:
        a_bound, b_bound = 0, alpha
    else:
        a_bound, b_bound = alpha, 0
    return golden_section_search(phi, a_bound, b_bound, tol)

