def dfp_method(f, grad, x0, eps=1e-6, max_iter=200):
    x = np.array(x0, dtype=float)
    G = np.eye(len(x))
    g = grad(x)
    history = [x.copy()]
    grad_norms = [np.linalg.norm(g)]

    for _ in range(max_iter):
        if np.linalg.norm(g) < eps:
            break
        d = -G @ g
        alpha = line_search(f, x, d)
        x_new = x + alpha * d
        g_new = grad(x_new)

        s, y = x_new - x, g_new - g
        sy = np.dot(s, y)
        if sy > 1e-10:
            G = G + np.outer(s, s)/sy - np.outer(G@y, G@y)/(np.dot(y, G@y)+1e-12)

        x, g = x_new, g_new
        history.append(x.copy())
        grad_norms.append(np.linalg.norm(g))

    return x, f(x), history, grad_norms

def bfgs_method(f, grad, x0, eps=1e-6, max_iter=200):
    x = np.array(x0, dtype=float)
    H = np.eye(len(x))
    g = grad(x)
    history = [x.copy()]
    grad_norms = [np.linalg.norm(g)]

    for _ in range(max_iter):
        if np.linalg.norm(g) < eps:
            break
        d = -H @ g
        alpha = line_search(f, x, d)
        x_new = x + alpha * d
        g_new = grad(x_new)

        s, y = x_new - x, g_new - g
        sy = np.dot(s, y)
        if sy > 1e-10:
            rho = 1.0 / sy
            I = np.eye(len(x))
            H = (I - rho*np.outer(s,y)) @ H @ (I - rho*np.outer(y,s)) + rho*np.outer(s,s)

        x, g = x_new, g_new
        history.append(x.copy())
        grad_norms.append(np.linalg.norm(g))

    return x, f(x), history, grad_norms

def fletcher_reeves(f, grad, x0, eps=1e-6, max_iter=200):
    x = np.array(x0, dtype=float)
    g = grad(x)
    d = -g.copy()
    history = [x.copy()]
    grad_norms = [np.linalg.norm(g)]

    for _ in range(max_iter):
        if np.linalg.norm(g) < eps:
            break
        alpha = line_search(f, x, d)
        x_new = x + alpha * d
        g_new = grad(x_new)

        beta = np.dot(g_new, g_new) / (np.dot(g, g) + 1e-12)
        d = -g_new + beta * d

        x, g = x_new, g_new
        history.append(x.copy())
        grad_norms.append(np.linalg.norm(g))

    return x, f(x), history, grad_norms

def polak_ribiere(f, grad, x0, eps=1e-6, max_iter=200):
    x = np.array(x0, dtype=float)
    g = grad(x)
    d = -g.copy()
    history = [x.copy()]
    grad_norms = [np.linalg.norm(g)]

    for _ in range(max_iter):
        if np.linalg.norm(g) < eps:
            break
        alpha = line_search(f, x, d)
        x_new = x + alpha * d
        g_new = grad(x_new)

        beta = np.dot(g_new, g_new - g) / (np.dot(g, g) + 1e-12)
        beta = max(0, beta)
        d = -g_new + beta * d

        x, g = x_new, g_new
        history.append(x.copy())
        grad_norms.append(np.linalg.norm(g))

    return x, f(x), history, grad_norms

