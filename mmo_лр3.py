def main():
    print("="*70)
    print("ЛАБОРАТОРНАЯ РАБОТА 3 - МЕТОДЫ МНОГОМЕРНОЙ ОПТИМИЗАЦИИ")
    print("Функция Розенброка")
    print("="*70)

    print(f"\nВариант 7: a={a}, b={b}, f0={f0}, n={n}")
    x0 = np.array([-1.2, 1.0])
    x_star = np.array([1.0, 1.0])

    print(f"Начальная точка: {x0}")
    print(f"f(x0) = {rosenbrock(x0):.6f}")
    print(f"\nТочный минимум: x* = {x_star}")
    print(f"f(x*) = {rosenbrock(x_star):.6f}")

    # 分析驻点
    H = np.array([[2*a*(6*1**2 - 2*1) + 2*b, -4*a*1],
                  [-4*a*1, 2*a]])
    eig = np.linalg.eigvals(H)
    print(f"Собственные значения гессиана: {eig[0]:.4f}, {eig[1]:.4f}")
    print("→ x* является локальным минимумом\n")

    # 运行所有方法
    methods = [
        ("DFP", dfp_method),
        ("BFGS", bfgs_method),
        ("Fletcher-Reeves", fletcher_reeves),
        ("Polak-Ribière", polak_ribiere)
    ]

    results = []
    grad_data = []
    func_data = []

    for name, method in methods:
        print(f"\n执行 {name}...")
        start = time.time()
        x_opt, f_opt, hist, grad_norms = method(rosenbrock, rosenbrock_grad, x0)
        elapsed = time.time() - start

        # 计算每次迭代的函数值
        f_values = [rosenbrock(x) for x in hist]

        print(f"  结果: x = ({x_opt[0]:.8f}, {x_opt[1]:.8f})")
        print(f"  f(x) = {f_opt:.8f}")
        print(f"  迭代次数: {len(hist)-1}")
        print(f"  耗时: {elapsed:.6f} 秒")
        print(f"  ||∇f|| = {grad_norms[-1]:.2e}")

        results.append({
            'name': name,
            'iter': len(hist)-1,
            'time': elapsed,
            'grad': grad_norms[-1],
            'f_opt': f_opt
        })

        grad_data.append((name, grad_norms))
        func_data.append((name, f_values))

        # 画轨迹图
        plot_trajectory(rosenbrock, hist, f"Траектория: {name}", x_star)

    # 对比图
    plot_comparison(grad_data)

    # 结果表格
    print("\n" + "="*70)
    print("ИТОГОВАЯ ТАБЛИЦА СРАВНЕНИЯ")
    print("="*70)
    print(f"{'Метод':<20} {'Итерации':<10} {'Время (с)':<12} {'||∇f||':<12} {'f(x*)':<12}")
    print("-"*70)
    for r in results:
        print(f"{r['name']:<20} {r['iter']:<10} {r['time']:<12.6f} {r['grad']:<12.2e} {r['f_opt']:<12.8f}")

    print("\n" + "="*70)
    print("РАБОТА ЗАВЕРШЕНА")
    print("="*70)

if __name__ == "__main__":
    main()