def plot_trajectory(f, history, title, x_star):
    history = [np.array(h).flatten() for h in history if len(np.array(h).flatten()) == 2]
    if len(history) < 2:
        return

    x1 = np.linspace(-1.5, 2.5, 200)
    x2 = np.linspace(-0.5, 3, 200)
    X1, X2 = np.meshgrid(x1, x2)
    Z = np.array([[f([x1, x2]) for x1 in x1] for x2 in x2])

    plt.figure(figsize=(10, 8))
    plt.contour(X1, X2, Z, levels=np.logspace(-1, 4, 30), cmap='viridis', norm=LogNorm())
    plt.colorbar(label='f(x)')

    hist = np.array(history)
    plt.plot(hist[:,0], hist[:,1], 'r.-', linewidth=2, markersize=6, label='Траектория')
    plt.plot(hist[0,0], hist[0,1], 'go', markersize=10, label='Старт')
    plt.plot(hist[-1,0], hist[-1,1], 'ro', markersize=10, label='Финиш')
    plt.plot(x_star[0], x_star[1], 'b*', markersize=15, label='Минимум (1,1)')

    plt.xlabel('x₁')
    plt.ylabel('x₂')
    plt.title(title)
    plt.legend()
    plt.grid(True, alpha=0.3)
    plt.show()

def plot_comparison(methods_data):
    plt.figure(figsize=(14, 5))

    # 左图：梯度收敛
    plt.subplot(1, 2, 1)
    for name, grad_norms in methods_data:
        plt.semilogy(range(len(grad_norms)), grad_norms, 'o-', linewidth=1.5, markersize=4, label=name)
    plt.xlabel('Итерация')
    plt.ylabel('||∇f||')
    plt.title('Сходимость по градиенту')
    plt.legend()
    plt.grid(True, alpha=0.3)

    # 右图：函数值收敛
    plt.subplot(1, 2, 2)
    for name, f_values in methods_data:
        plt.semilogy(range(len(f_values)), f_values, 'o-', linewidth=1.5, markersize=4, label=name)
    plt.xlabel('Итерация')
    plt.ylabel('f(x)')
    plt.title('Сходимость по функции')
    plt.legend()
    plt.grid(True, alpha=0.3)

    plt.suptitle('Сравнение методов оптимизации')
    plt.tight_layout()
    plt.show()

