# ============================================================
# 1. Параметры (Вариант 7: a=2, b=2, f0=10, n=2)
# ============================================================
a, b, f0, n = 2, 2, 10, 2

def rosenbrock(x):
    x = np.array(x, dtype=float)
    f = 0
    for i in range(n-1):
        f += a * (x[i]**2 - x[i+1])**2 + b * (x[i] - 1)**2
    return f + f0

def rosenbrock_grad(x):
    x = np.array(x, dtype=float)
    grad = np.zeros(n)
    grad[0] = 2 * a * (x[0]**2 - x[1]) * (2*x[0]) + 2 * b * (x[0] - 1)
    grad[1] = -2 * a * (x[0]**2 - x[1])
    return grad

